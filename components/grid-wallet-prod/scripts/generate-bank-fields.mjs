#!/usr/bin/env node
// Generates src/data/bankAccountFields.generated.ts from the Grid OpenAPI spec.
//
// Accuracy guard: this demo ships next to the docs, so per-country bank fields
// must be 1:1 with the API. Field facts (keys, required, patterns, enums, and
// even sample values) are DERIVED from openapi/ via a Redocly bundle - never
// hand-authored or recalled. Regenerate after any spec change:
//   npm run gen:bank-fields
// and `npm run verify:bank-fields` fails CI if the committed output drifts.

import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { tmpdir } from 'node:os';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '../../..');
const outFile = resolve(scriptDir, '../src/data/bankAccountFields.generated.ts');
const tmpJson = join(tmpdir(), `grid-bundle-${process.pid}.json`);

// Crypto wallets are the demo's separate "Crypto wallet" path, not the fiat
// bank picker - exclude them here.
const CRYPTO = new Set([
  'SPARK_WALLET',
  'LIGHTNING',
  'SOLANA_WALLET',
  'TRON_WALLET',
  'POLYGON_WALLET',
  'BASE_WALLET',
  'ETHEREUM_WALLET',
]);

// Handled by the POST builder, not shown as editable account fields:
// accountType is the discriminator; beneficiary is a uniform sub-object.
const OMIT_FIELDS = new Set(['accountType', 'beneficiary']);

console.log('Bundling spec via Redocly...');
execSync(`npx --no-install @redocly/cli bundle openapi/openapi.yaml -o "${tmpJson}"`, {
  cwd: repoRoot,
  stdio: ['ignore', 'ignore', 'inherit'],
});

const spec = JSON.parse(readFileSync(tmpJson, 'utf8'));
const schemas = spec.components?.schemas;
if (!schemas) throw new Error('No components.schemas in bundled spec');

const refName = (ref) => (typeof ref === 'string' ? ref.split('/').pop() : null);
const deref = (node) => (node && node.$ref ? schemas[refName(node.$ref)] : node);

const oneOf = schemas.ExternalAccountCreateInfoOneOf;
const mapping = oneOf?.discriminator?.mapping;
if (!mapping) throw new Error('No ExternalAccountCreateInfoOneOf discriminator mapping');

function collectFields(createInfo) {
  const required = new Set();
  const props = {};
  let example = {};
  for (const memberRaw of createInfo.allOf ?? []) {
    const member = deref(memberRaw);
    if (!member) continue;
    (member.required ?? []).forEach((r) => required.add(r));
    Object.assign(props, member.properties ?? {});
    if (member.example && typeof member.example === 'object') {
      example = { ...example, ...member.example };
    }
  }

  const fields = [];
  for (const [key, propRaw] of Object.entries(props)) {
    if (OMIT_FIELDS.has(key)) continue;
    const prop = deref(propRaw) ?? propRaw;
    const field = { key, required: required.has(key), kind: prop.enum ? 'select' : 'text' };
    if (prop.enum) field.enum = prop.enum;
    if (prop.pattern) field.pattern = prop.pattern;
    if (typeof prop.minLength === 'number') field.minLength = prop.minLength;
    if (typeof prop.maxLength === 'number') field.maxLength = prop.maxLength;
    if (prop.description) field.description = prop.description;
    const ex = prop.example ?? example[key];
    if (ex !== undefined) field.example = String(ex);
    fields.push(field);
  }
  return fields;
}

const out = {};
for (const [accountType, ref] of Object.entries(mapping)) {
  if (CRYPTO.has(accountType)) continue;
  const createInfo = schemas[refName(ref)];
  if (!createInfo) throw new Error(`Missing create-info schema for ${accountType}`);
  out[accountType] = {
    accountType,
    currency: accountType.replace(/_ACCOUNT$/, ''),
    fields: collectFields(createInfo),
  };
}

const sorted = Object.fromEntries(Object.keys(out).sort().map((k) => [k, out[k]]));

const header = `// GENERATED by scripts/generate-bank-fields.mjs from the Grid OpenAPI spec.
// DO NOT EDIT BY HAND. Field facts (keys, required, patterns, enums, examples)
// are derived 1:1 from openapi/ so the demo stays accurate next to the docs.
// Regenerate:  npm run gen:bank-fields   |   Verify (CI):  npm run verify:bank-fields
//
// Every fiat external account also requires a \`beneficiary\` (INDIVIDUAL
// { fullName } or BUSINESS { legalName }) - added by the POST builder, not here.
/* eslint-disable */

export type BankFieldKind = 'text' | 'select';

export interface BankFieldSpec {
  key: string;
  required: boolean;
  kind: BankFieldKind;
  enum?: string[];
  pattern?: string;
  minLength?: number;
  maxLength?: number;
  description?: string;
  example?: string;
}

export interface BankAccountSchema {
  /** Discriminator value, e.g. "USD_ACCOUNT". */
  accountType: string;
  /** ISO 4217 currency (the accountType prefix), e.g. "USD". */
  currency: string;
  /** Country-specific account fields (excludes accountType + beneficiary). */
  fields: BankFieldSpec[];
}

export const BANK_ACCOUNT_SCHEMAS: Record<string, BankAccountSchema> = ${JSON.stringify(sorted, null, 2)};
`;

writeFileSync(outFile, header);
console.log(`Wrote ${Object.keys(sorted).length} account types -> ${outFile}`);
